def sum_of_arguments(x, y):
    '''
    This is a summation of two arguments.

    Args:
        x: the first number chosen.
        y: the second number chosen.

    Returns:
        sum: addition of the two selected arguments.
    '''
    sum = x + y
    return sum

def product_of_arguments(x, y):
    '''
    This is a product of two arguments.

    Args:
        x: the first number chosen.
        y: the second number chosen.

    Returns:
        prod: mulitplication of the two selected arguments.
    '''
    prod = x * y
    return prod

